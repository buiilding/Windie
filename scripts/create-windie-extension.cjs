#!/usr/bin/env node
/**
 * Runs the create windie extension workflow for the developer CLI and automation tooling.
 */

'use strict';

const fs = require('fs');
const path = require('path');

const TOOL_NAME_PATTERN = /^[a-zA-Z][a-zA-Z0-9_-]{0,95}$/;
const EXTENSION_ID_PATTERN = /^[a-z0-9][a-z0-9_-]{1,63}$/;

function usage() {
  return [
    'Usage: <windie> extension create <plugin-id> [options]',
    '',
    '<windie> is bin\\windie.cmd on Windows PowerShell/CMD and bin/windie.sh on macOS/Linux.',
    '',
    'Options:',
    '  --dir <path>       Contribution root. Defaults to .',
    '  --name <name>      Display name. Defaults to a title-cased plugin id',
    '  --tool <name>      Sample tool name. Defaults to sample_tool',
    '  --force            Allow writing into existing empty contribution folders',
    '  -h, --help         Show this help',
  ].join('\n');
}

function toTitleCase(value) {
  return value
    .split(/[-_\s]+/)
    .filter(Boolean)
    .map(part => `${part.charAt(0).toUpperCase()}${part.slice(1)}`)
    .join(' ');
}

function parseArgs(argv) {
  const options = {
    contributionsDir: '.',
    force: false,
    toolName: 'sample_tool',
  };
  const positional = [];

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === '-h' || arg === '--help') {
      options.help = true;
    } else if (arg === '--force') {
      options.force = true;
    } else if (arg === '--dir') {
      index += 1;
      options.contributionsDir = argv[index];
    } else if (arg === '--name') {
      index += 1;
      options.name = argv[index];
    } else if (arg === '--tool') {
      index += 1;
      options.toolName = argv[index];
    } else if (arg.startsWith('--')) {
      throw new Error(`Unknown option: ${arg}`);
    } else {
      positional.push(arg);
    }
  }

  if (options.help) {
    return options;
  }
  if (positional.length !== 1) {
    throw new Error('Expected exactly one plugin id.');
  }
  options.extensionId = positional[0];
  return options;
}

function validateOptions(options) {
  if (!options.extensionId || !EXTENSION_ID_PATTERN.test(options.extensionId)) {
    throw new Error('Plugin id must match /^[a-z0-9][a-z0-9_-]{1,63}$/.');
  }
  if (!options.toolName || !TOOL_NAME_PATTERN.test(options.toolName)) {
    throw new Error('Tool name must match /^[a-zA-Z][a-zA-Z0-9_-]{0,95}$/.');
  }
  if (!options.contributionsDir) {
    throw new Error('Contribution root is required.');
  }
}

function writeText(filePath, value) {
  fs.writeFileSync(filePath, value, 'utf8');
}

function assertCanCreate(targetDir, force) {
  if (!fs.existsSync(targetDir)) {
    return;
  }
  const entries = fs.readdirSync(targetDir);
  if (entries.length > 0 || !force) {
    throw new Error(
      `Extension contribution folder already exists: ${targetDir}. Use --force only for an empty folder.`,
    );
  }
}

function buildFiles({ extensionId, name, toolName }) {
  const displayName = name || toTitleCase(extensionId);
  return {
    pluginFiles: new Map([
    ['plugin.json', JSON.stringify({
      id: extensionId,
      name: displayName,
      description: `Starter local-runtime plugin for ${displayName}.`,
      tools: [
        {
          name: toolName,
          description: `Run the starter ${displayName} local tool.`,
          schema: `schemas/${toolName}.schema.json`,
          entrypoint: `python/${toolName}.py:run`,
          argument_resolution: 'passthrough',
        },
      ],
      required_permissions: [],
    }, null, 2) + '\n'],
    [`schemas/${toolName}.schema.json`, JSON.stringify({
      type: 'object',
      additionalProperties: false,
      properties: {
        text: {
          type: 'string',
          description: 'Text for the starter tool to process.',
        },
      },
      required: ['text'],
    }, null, 2) + '\n'],
    [`python/${toolName}.py`, [
      'from __future__ import annotations',
      '',
      'from tools.result import ToolResult',
      '',
      '',
      'async def run(text: str):',
      '    value = str(text or "").strip()',
      '    if not value:',
      '        return ToolResult.error_result("text is required")',
      '    return ToolResult.success_result(',
      '        {',
      '            "output": f"Starter tool received: {value}",',
      '        }',
      '    )',
      '',
    ].join('\n')],
    ['README.md', [
      `# ${displayName} Plugin`,
      '',
      'Starter local-runtime plugin generated by `<windie> extension create`.',
      '',
      '## Contents',
      '',
      `- \`plugin.json\` declares the local-runtime plugin and the \`${toolName}\` tool.`,
      `- \`schemas/${toolName}.schema.json\` is the model-facing JSON Schema.`,
      `- \`python/${toolName}.py\` is the Python entrypoint executed by the local runtime.`,
      '',
      '## Use',
      '',
      'Keep this folder under the repo-level `plugins/` directory.',
      '',
    ].join('\n')],
    ['docs/README.md', [
      `# ${displayName} Plugin Notes`,
      '',
      'Document plugin-specific behavior, permissions, config, and maintenance notes here.',
      '',
    ].join('\n')],
  ]),
    skillFiles: new Map([
    ['SKILL.md', [
      '---',
      `title: ${displayName} Agent`,
      'priority: 75',
      '---',
      '',
      `Use the ${displayName} extension tools when they directly help with the user request.`,
      'Keep tool calls narrow, explain failures plainly, and prefer local workspace context when available.',
      '',
    ].join('\n')],
    ['README.md', [
      `# ${displayName} Skill`,
      '',
      'Starter prompt skill paired with the local-runtime plugin.',
      '',
    ].join('\n')],
  ]),
  };
}

function createWindieExtension(rawOptions) {
  const options = {
    contributionsDir: rawOptions.contributionsDir || '.',
    force: Boolean(rawOptions.force),
    toolName: rawOptions.toolName || 'sample_tool',
    ...rawOptions,
  };
  validateOptions(options);

  const contributionsDir = path.resolve(options.contributionsDir);
  const pluginDir = path.join(contributionsDir, 'plugins', options.extensionId);
  const skillDir = path.join(contributionsDir, 'skills', options.extensionId);
  assertCanCreate(pluginDir, options.force);
  assertCanCreate(skillDir, options.force);

  fs.mkdirSync(pluginDir, { recursive: true });
  fs.mkdirSync(skillDir, { recursive: true });
  const files = buildFiles(options);
  for (const [relativePath, contents] of files.pluginFiles) {
    const filePath = path.join(pluginDir, relativePath);
    fs.mkdirSync(path.dirname(filePath), { recursive: true });
    writeText(filePath, contents);
  }
  for (const [relativePath, contents] of files.skillFiles) {
    const filePath = path.join(skillDir, relativePath);
    fs.mkdirSync(path.dirname(filePath), { recursive: true });
    writeText(filePath, contents);
  }

  return {
    extensionDir: pluginDir,
    pluginDir,
    skillDir,
    extensionId: options.extensionId,
    toolName: options.toolName,
  };
}

function runCli(argv = process.argv.slice(2)) {
  const options = parseArgs(argv);
  if (options.help) {
    process.stdout.write(`${usage()}\n`);
    return 0;
  }
  const result = createWindieExtension(options);
  process.stdout.write(`Created local-runtime plugin at ${result.pluginDir}\n`);
  process.stdout.write(`Created prompt skill at ${result.skillDir}\n`);
  process.stdout.write(`Tool: ${result.toolName}\n`);
  return 0;
}

if (require.main === module) {
  try {
    process.exitCode = runCli();
  } catch (error) {
    process.stderr.write(`${error instanceof Error ? error.message : String(error)}\n\n`);
    process.stderr.write(`${usage()}\n`);
    process.exitCode = 1;
  }
}

module.exports = {
  buildFiles,
  createWindieExtension,
  parseArgs,
  runCli,
  toTitleCase,
};
